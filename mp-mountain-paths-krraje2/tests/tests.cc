size_t MinVal(size_t d1, size_t d2, size_t d3) {
  std::vector<size_t> changes{d1, d2, d3};
  size_t min = d1;
  for (size_t c = 0; c < changes.size(); c++) {
    if (changes.at(c) < min) {
      min = changes.at(c);
    }
  }
  return min;
}
